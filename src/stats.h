#pragma once

#include <cstddef>
#include <cstdint>

struct solver_stats {
	std::uint64_t nodes = 0;				// Число посещённых состояний 
	std::uint64_t leaves = 0;				// Число терминальных состояний (полных решений)
	std::uint64_t pruned_by_bound = 0;		// Число отсечённых ветвей по границе
	std::uint64_t pruned_by_memo_exact = 0; // Число отсечённых ветвей по точному значению из мемоизации
	std::uint64_t pruned_by_memo_lb = 0;	// Число отсечённых ветвей из-за усиленной ниженей границы из мемоизации
	std::uint64_t memo_hits = 0;			// Число успешных обращений к мемоизации (найдено значение для текущего состояния)
	std::uint64_t memo_misses = 0;			// Число неуспешных обращений к мемоизации (нет значения для текущего состояния)
	std::uint64_t memo_inserts = 0;			// Число добавленных в мемоизацию новых записей
	std::uint64_t memo_updates = 0;			// Число обновлений существующих записей в мемоизации (улучшение найденного решения)
	std::uint64_t memo_evictions = 0;		// Число удалённых из мемоизации записей
	std::size_t memo_peak_size = 0;			// Максимальный размер таблицы мемоизации 
	std::size_t memo_final_size = 0;		// Размер таблицы мемоизации в конце решения
	double elapsed_ms = 0.0;				// Затраченное время в миллисекундах
};
